package com.techprimers.jpa.springjpahibernateexample.model;

public class AccountTrailer {
	private String AccountControlTotal;
	private String NumberofRecords;
	private String EndAccountTrailer="/";
	public String getEndAccountTrailer() {
		return EndAccountTrailer;
	}
	public void setEndAccountTrailer(String endAccountTrailer) {
		EndAccountTrailer = endAccountTrailer;
	}
	public String getAccountControlTotal() {
		return AccountControlTotal;
	}
	public void setAccountControlTotal(String accountControlTotal) {
		AccountControlTotal = accountControlTotal;
	}
	public String getNumberofRecords() {
		return NumberofRecords;
	}
	public void setNumberofRecords(String numberofRecords) {
		NumberofRecords = numberofRecords;
	}
	


}
